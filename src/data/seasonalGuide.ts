/**
 * Get the seasonal tip for the current month.
 */
export function getCurrentSeasonalTip(): SeasonalTip {
  const month = new Date().getMonth() + 1; // 1-12
  return SEASONAL_GUIDE.find((s) => s.month === month) ?? SEASONAL_GUIDE[0];
}

/**
 * Get seasonal tips relevant to the coming 3 months.
 */
export function getUpcomingSeasonalTips(): SeasonalTip[] {
  const month = new Date().getMonth() + 1;
  return [0, 1, 2].map((offset) => {
    const m = ((month - 1 + offset) % 12) + 1;
    return SEASONAL_GUIDE.find((s) => s.month === m) ?? SEASONAL_GUIDE[0];
  });
}
